/// Track outlines for the five real circuits come from the open f1-circuits
/// dataset (github.com/bacinger/f1-circuits, MIT), simplified with
/// Ramer-Douglas-Peucker and normalised to each track's bounding box.
/// The custom test track stays a stylised loop. Rendering is aspect-correct:
/// `aspect` is the track's width/height and TrackOutlineShape letterboxes
/// into whatever rect it's given.
enum TrackOutlines {
    struct Outline {
        let points: [CGPoint]
        let aspect: CGFloat
        let smoothed: Bool

        init(aspect: Double, smoothed: Bool, raw: [(Double, Double)]) {
            self.points = raw.map { CGPoint(x: $0.0, y: $0.1) }
            self.aspect = aspect
            self.smoothed = smoothed
        }
    }

    static func outline(for circuitID: String) -> Outline {
        switch circuitID {
        case "monteCarlo": return monteCarlo
        case "marina": return marina
        case "midlands": return midlands
        case "hachi": return hachi
        case "ardennes": return ardennes
        case "royalPark": return royalPark
        case "bayCity": return bayCity
        case "loneStar": return loneStar
        case "serraVerde": return serraVerde
        case "caspian": return caspian
        case "dunePark": return dunePark
        case "alpine": return alpine
        default: return custom
        }
    }

