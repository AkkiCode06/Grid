/// Draws a circuit's outline aspect-fit and centred inside `rect`. Use
/// `.trim(from:to:)` on it for the lap-progress fill.
struct TrackOutlineShape: Shape {
    let circuitID: String

    func path(in rect: CGRect) -> Path {
        let outline = TrackOutlines.outline(for: circuitID)
        var width = rect.width
        var height = width / outline.aspect
        if height > rect.height {
            height = rect.height
            width = height * outline.aspect
        }
        let originX = rect.midX - width / 2
        let originY = rect.midY - height / 2
        let points = outline.points.map { point in
            CGPoint(x: originX + point.x * width, y: originY + point.y * height)
        }
        if outline.smoothed {
            return Path.catmullRomClosed(through: points)
        }
        var path = Path()
        path.addLines(points)
        path.closeSubpath()
        return path
    }
}

extension Path {
    /// Closed Catmull-Rom spline through the given points, converted to
    /// cubic Bezier segments.
    static func catmullRomClosed(through points: [CGPoint]) -> Path {
        var path = Path()
        guard points.count > 2 else {
            path.addLines(points)
            path.closeSubpath()
            return path
        }
        let n = points.count
        path.move(to: points[0])
        for i in 0..<n {
            let p0 = points[(i - 1 + n) % n]
            let p1 = points[i]
            let p2 = points[(i + 1) % n]
            let p3 = points[(i + 2) % n]
            let c1 = CGPoint(x: p1.x + (p2.x - p0.x) / 6,
                             y: p1.y + (p2.y - p0.y) / 6)
            let c2 = CGPoint(x: p2.x - (p3.x - p1.x) / 6,
                             y: p2.y - (p3.y - p1.y) / 6)
            path.addCurve(to: p2, control1: c1, control2: c2)
        }
        path.closeSubpath()
        return path
    }
}
